# Arquivo para aprender a realizar cáuculos com python

# Sinais matemáticos:       Serve para
# + Soma                    Serve para somar valores
# - Subtração               Serve para diminuir um valor do outro
# * Multiplicação           Serve para multiplicar valores
# / Divisão                 Serve para dividir um valor para o outro
# ** Potência               Serve para fazer a potência de valores
# // Divisão inteira        Serve para retornar a parte inteira da divisão
# % resto da divisão        Serve para retornar o resto da divisão

numero_1 = int(input("Digite o primeiro valor: "))
numero_2 = int(input("Digite o segundo valor: "))

resultado = numero_1 + numero_2
print("A soma do numero 1 com o numero 2 é: ",resultado)

resultado = numero_1 - numero_2
print("A subtração do numero 1 com o numero 2 é: ",resultado)

resultado = numero_1 * numero_2
print("A multiplicação do numero 1 com o numero 2 é: ",resultado)

resultado = numero_1 / numero_2
print("A divisão do numero 1 com o numero 2 é: ",resultado)

resultado = numero_1 ** numero_2
print("A potência do numero 1 com o numero 2 é: ",resultado)

resultado = numero_1 // numero_2
print("A divisão inteira do numero 1 com o numero 2 é: ",resultado)

resultado = numero_1 % numero_2
print("O resto da divisão do numero 1 com o numero 2 é: ",resultado)
